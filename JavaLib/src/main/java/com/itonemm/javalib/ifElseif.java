package com.itonemm.javalib;

public class ifElseif {
    public static void main(String[]args)
    {
        int i=0;
        if(i<10)
        {
            System.out.println("i<10");
        }
        else if(i<15)
        {
            System.out.println("i <15");
        }
        else if(i<20)
        {
            System.out.println("i <20");
        }
        else {
            System.out.println(" i not in range");
        }

    }

}
